    public function store(Request $request)
    {
        //
        $x=new Guest();
        $x->name=$request->name;
        $x->phone=$request->phone;
        $x->email=$request->email;
        $x->address=$request->address;
        $x->msg=$request->msg;
        $x->save();
        return redirect()->route('index');
    }

